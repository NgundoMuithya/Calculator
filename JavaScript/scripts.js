function add() {
    var number1 = prompt("Enter a number:");
    let number2 = prompt("Enter another number:");
    return parseFloat(number1) + parseInt(number2);
} 

function subtract() {
    var number1 = prompt("Enter a number:");
    var number2 = prompt("Enter another number:");
    return parseFloat(number1) - parseFloat(number2);  
}
